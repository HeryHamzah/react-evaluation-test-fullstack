// Fungsi helper untuk format harga
export const formatPrice = (price) => {
  return new Intl.NumberFormat('id-ID', {
    style: 'currency',
    currency: 'IDR',
    minimumFractionDigits: 0,
    maximumFractionDigits: 0
  }).format(price).replace('IDR', 'Rp');
};

// Fungsi untuk mendapatkan semua produk
export const getAllProducts = () => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(mockFurnitureProducts);
    }, 300); // Simulasi delay API
  });
};

// Fungsi untuk search produk
export const searchProducts = (query) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      const filtered = mockFurnitureProducts.filter(product =>
        product.name.toLowerCase().includes(query.toLowerCase()) ||
        product.category.toLowerCase().includes(query.toLowerCase())
      );
      resolve(filtered);
    }, 300);
  });
};

// Fungsi untuk filter berdasarkan kategori
export const getProductsByCategory = (category) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      const filtered = mockFurnitureProducts.filter(product =>
        product.category === category
      );
      resolve(filtered);
    }, 300);
  });
};
